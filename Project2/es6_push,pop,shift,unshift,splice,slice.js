//push,pop,shift,unshift,splice,slice

// push คือ การนำสมาชิกมาต่อท้าย Array
const data = [10,20,30]
data.push(500,2000)
console.log(data)

// pop คือ การนำสมาชิกท้ายสุดออก
data.pop()
console.log(data)

// unshift คือ เพิ่มสมาชิกด้านหน้าสุดของ Array
data.unshift(5)
console.log(data)

// shift คือ ลบสมาชิกตัวด้านหน้าออก
data.shift()
console.log(data)

